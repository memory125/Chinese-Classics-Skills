    def __init__(self, target_date=None):
        """
        初始化节气计算器
        :param target_date: 目标日期，默认今天
        """
        if target_date is None:
            self.target_date = date.today()
        elif isinstance(target_date, str):
            self.target_date = datetime.strptime(target_date, "%Y-%m-%d").date()
        else:
            self.target_date = target_date
        
        self.year = self.target_date.year
        
        # 检查是否支持该年份
        if self.year not in self.YEAR_DATA:
            raise ValueError(f"暂不支持 {self.year} 年的节气查询，当前支持 2024-2030 年")
        
        self.terms = self.YEAR_DATA[self.year]
    
    def get_current_solar_term(self):
        """
        获取当前日期所在的节气
        :return: dict containing solar term info
        """
        target = self.target_date
        
        current_term = None
        next_term = None
        days_since_last = 0
        days_to_next = float('inf')
        
        for i, (term_name, month, day, hour, minute) in enumerate(self.terms):
            try:
                term_date = date(target.year, month, day)
                
                if month < target.month or (month == target.month and day <= target.day):
                    current_term = term_name
                    days_since_last = (target - term_date).days
                    
                    # 计算下一个节气
                    if i + 1 < len(self.terms):
                        next_term_info = self.terms[i + 1]
                        next_month, next_day = next_term_info[1], next_term_info[2]
                        
                        # 处理跨年
                        if next_month < month or (next_month == month and next_day < day):
                            next_date = date(target.year + 1, next_month, next_day)
                        else:
                            next_date = date(target.year, next_month, next_day)
                        
                        days_to_next = (next_date - target).days
                        next_term = next_term_info[0]
                        
            except ValueError:
                continue
        
        if not current_term:
            current_term = "大寒"
            days_since_last = 0
            days_to_next = 15
        
        season_info = self.SEASONS.get(current_term, ("四季", "养生季"))
        
        return {
            "current_solar_term": current_term,
            "season": season_info[0],
            "nurturing_focus": season_info[1],
            "days_since_last_term": days_since_last,
            "days_to_next_term": days_to_next if days_to_next != float('inf') else None,
            "next_solar_term": next_term
        }
    
